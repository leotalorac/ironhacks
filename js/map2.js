function putMarker(position,icon,title){
    var marker = new google.maps.Marker({
    position:position,
    map: map,
    icon: icon,
    title:title    
    });
    mapmarkers.push(marker);
}
function putPoligon(coords,parent){
    var tempol = new google.maps.Polygon({
        paths: coords,
        strokeColor: '#C29CFF',
        strokeOpacity: 0.8,
        strokeWeight: 2,
        fillColor: '#C29CFF',
        fillOpacity: 0.35,
        map
    });
    parent.push(tempol);
}


